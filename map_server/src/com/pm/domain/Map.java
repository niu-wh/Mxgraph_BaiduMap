package com.pm.domain;

public class Map {
    private int id;
    private String city;
    private String stab;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getCity() {
        return city;
    }

    public void setCity(String city) {
        this.city = city;
    }

    public String getStab() {
        return stab;
    }

    public void setStab(String stab) {
        this.stab = stab;
    }
}
